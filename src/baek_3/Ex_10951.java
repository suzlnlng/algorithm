package baek_3;

import java.util.Scanner;

public class Ex_10951 {

	public static void main(String[] args) throws Exception{
		
		Scanner sc = new Scanner(System.in);
		
		try {
			
			while(true) {
				
				int a = sc.nextInt();
				int b = sc.nextInt();
				
				System.out.println(a+b);
				sc.nextLine();
				
			}
			
		} catch (Exception e) {
			return;
		}
		
	}

}
